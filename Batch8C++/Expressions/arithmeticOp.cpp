#include<iostream>
using namespace std; 
int main() 
{
     int a,b;
     a = 4;
     b = 2;

     cout<<a  <<" + "<< b << " = "<<(a + b)<<"\n";// 4 + 2 = 6
     cout<<a  <<" - "<< b << " = "<<(a - b)<<endl;// 4 - 2 = 6
     cout<<a  <<" * "<< b << " = "<<(a * b)<<"\n";// 4 * 2 = 6
     cout<<a  <<" / "<< b << " = "<<(a / b)<<"\n";// 4 / 2 = 6
     cout<<a  <<" % "<< b << " = "<<(a % b)<<"\n";// 4 % 2 = 6
     return 0;
}